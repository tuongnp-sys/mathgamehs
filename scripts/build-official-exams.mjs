function mc(id, part, order, page, stemVi, stemEn, options, explanationVi, explanationEn) {
  return {
    id,
    part,
    order,
    source: id.startsWith('off26') ? 'official-thpt-2026-0116' : 'official-thpt-2025-0101',
    status: 'approved',
    examImage: page,
    stem: bi(stemVi, stemEn),
    options: options.map((o) => ({
      label: o.label,
      text: bi(o.vi, o.en),
      correct: o.correct,
    })),
    explanation: bi(explanationVi, explanationEn),
  };
}

function tf(id, part, order, page, stemVi, stemEn, items) {
  return {
    id,
    part,
    order,
    source: id.startsWith('off26') ? 'official-thpt-2026-0116' : 'official-thpt-2025-0101',
    status: 'approved',
    examImage: page,
    stem: bi(stemVi, stemEn),
    subItems: items.map((it) => ({
      label: it.label,
      statement: bi(it.vi, it.en),
      isTrue: it.isTrue,
      explanation: bi(it.explVi, it.explEn),
    })),
  };
}

